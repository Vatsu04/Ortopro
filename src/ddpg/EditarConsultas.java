private void listarValores() { //Declaração do método listarValores(). Esse método é provavelmente parte de uma classe e é responsável por preencher uma tabela ou lista com valores obtidos de algum lugar, como um banco de dados.
        try {
            ConsultasDAO objConsultasDAO = new ConsultasDAO();
            DefaultTableModel model = (DefaultTableModel) TabelaConsultas.getModel();
            model.setNumRows(0);

            ArrayList<Consultas> lista = objConsultasDAO.PesquisarConsulta();

            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getId_Consulta(),
                     lista.get(num).getId_Paciente(),
                   lista.get(num).getHorario(),
                    lista.get(num).getData_Consulta()
                });

            }

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Listar Valores :" + erro);
        }
    }

  private void CarregarCampos() {
/*
        Esse método é responsável por carregar os campos de texto da interface com os valores selecionados na tabela. Ele faz o seguinte:

Obtém o índice da linha selecionada na tabela através do método getSelectedRow().
Através do modelo da tabela (TabelaConsultass.getModel()), obtém os valores correspondentes da linha selecionada.
Define os valores obtidos nos campos de texto Id, User, Telefone e Email.
 */        
        int setar = TabelaConsultas.getSelectedRow();

        ID.setText(TabelaConsultas.getModel().getValueAt(setar, 0).toString());
        
         ID_Paciente.setText(TabelaConsultas.getModel().getValueAt(setar, 1 ).toString());
         
        Horario.setText(TabelaConsultas.getModel().getValueAt(setar, 2).toString());
        Data_Consulta.setText(TabelaConsultas.getModel().getValueAt(setar, 3).toString());
        
     
    }

