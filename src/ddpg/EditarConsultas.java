    private void AlterarConsultas() {
 /*
 Esse método é responsável por alterar as informações de um usuário. Ele faz o seguinte:

Obtém os valores dos campos Id, User, Telefone e Email.
Cria uma instância da classe Consultass e define os valores obtidos nos respectivos atributos.
Cria uma instância da classe ConsultassDAO.
Chama o método AlterarConsultass() da classe ConsultassDAO, passando o objeto objConsultass como argumento para realizar a alteração no banco de dados.   
 */        

        int id_consulta;
        int id_paciente;
        String horario;
        String data_consulta;
        
        
        
        
        
        

        id_consulta = Integer.parseInt(ID.getText());
      id_paciente = Integer.parseInt(ID_Paciente.getText());
          horario = Horario.getText();
        data_consulta = Data_Consulta.getText();
      
        
  
        
        Consultas objConsultass = new Consultas();
        objConsultass.setId_Consulta(id_consulta);
       
         objConsultass.setId_Paciente(id_paciente);
        objConsultass.setHorario(horario);
        objConsultass.setData_Consulta(data_consulta);
        

        ConsultasDAO objConsultassDAO = new ConsultasDAO();
        objConsultassDAO.AlterarConsultas(objConsultass);

    }

    private void ExcluirConsultas() {
 /*
 Esse método é responsável por excluir um usuário. Ele faz o seguinte:

Obtém o valor do campo Id e converte para o tipo inteiro.
Cria uma instância da classe Consultas e define o valor do ID obtido no atributo id.
Cria uma instância da classe ConsultasDAO.
Chama o método ExcluirConsultas() da classe ConsultasDAO, passando o objeto objConsultas como argumento para realizar a exclusão no banco de dados.   
 */            
        int id = 0;

        id = Integer.parseInt(ID.getText());

        Consultas objConsultas = new Consultas();
        objConsultas.setId_Consulta(id);

        ConsultasDAO objConsultasDAO = new ConsultasDAO();
        objConsultasDAO.ExcluirConsultas(objConsultas);
    }




