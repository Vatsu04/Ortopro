    private void AlterarProntuarios() {
 /*
 Esse método é responsável por alterar as informações de um usuário. Ele faz o seguinte:

Obtém os valores dos campos Id, User, Telefone e Email.
Cria uma instância da classe Prontuarios e define os valores obtidos nos respectivos atributos.
Cria uma instância da classe ProntuariosDAO.
Chama o método AlterarProntuarios() da classe ProntuariosDAO, passando o objeto objProntuarios como argumento para realizar a alteração no banco de dados.   
 */        

         int id_prontuario;
        int id_paciente;
        String diagnostico;
        String retorno;
        String remedios_prescritos;
        int id_consulta;
        
        
        

        id_prontuario = Integer.parseInt(ID.getText());
        
         id_paciente = Integer.parseInt(ID_Paciente.getText());
          retorno = Retorno.getText();
        remedios_prescritos = Remedios.getText();
        id_consulta = Integer.parseInt(ID_Consulta.getText());
        diagnostico = Diagnostico.getText();
        
        
        Prontuario objProntuarios = new Prontuario();
        objProntuarios.setId_Prontuario(id_prontuario);
        objProntuarios.setDiagnostico(diagnostico);
         objProntuarios.setId_Paciente(id_paciente);
        objProntuarios.setRetorno(retorno);
        objProntuarios.setRemedios_Prescritos(remedios_prescritos);
        objProntuarios.setId_Consulta(id_consulta);

        ProntuarioDAO objProntuariosDAO = new ProntuarioDAO();
        objProntuariosDAO.AlterarProntuario(objProntuarios);

    }

    private void ExcluirProntuario() {
 /*
 Esse método é responsável por excluir um usuário. Ele faz o seguinte:

Obtém o valor do campo Id e converte para o tipo inteiro.
Cria uma instância da classe Prontuario e define o valor do ID obtido no atributo id.
Cria uma instância da classe ProntuarioDAO.
Chama o método ExcluirProntuario() da classe ProntuarioDAO, passando o objeto objProntuario como argumento para realizar a exclusão no banco de dados.   
 */            
        int id = 0;

        id = Integer.parseInt(ID.getText());

        Prontuario objProntuario = new Prontuario();
        objProntuario.setId_Prontuario(id);

        ProntuarioDAO objProntuarioDAO = new ProntuarioDAO();
        objProntuarioDAO.ExcluirProntuario(objProntuario);
    }


