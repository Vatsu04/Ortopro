private void listarValores() { //Declaração do método listarValores(). Esse método é provavelmente parte de uma classe e é responsável por preencher uma tabela ou lista com valores obtidos de algum lugar, como um banco de dados.
        try {
            ProntuarioDAO objProntuarioDAO = new ProntuarioDAO();
            DefaultTableModel model = (DefaultTableModel) TabelaProntuario.getModel();
            model.setNumRows(0);

            ArrayList<Prontuario> lista = objProntuarioDAO.PesquisarProntuario();

            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getId_Prontuario(),
                     lista.get(num).getId_Consulta(),
                   
                   lista.get(num).getDiagnostico(),
                   
                    lista.get(num).getRemedios_Prescritos(),
                     lista.get(num).getRetorno(),
                     lista.get(num).getId_Paciente(),
                  
                });

            }

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Listar Valores :" + erro);
        }
    }

  private void CarregarCampos() {
/*
        Esse método é responsável por carregar os campos de texto da interface com os valores selecionados na tabela. Ele faz o seguinte:

Obtém o índice da linha selecionada na tabela através do método getSelectedRow().
Através do modelo da tabela (TabelaProntuarios.getModel()), obtém os valores correspondentes da linha selecionada.
Define os valores obtidos nos campos de texto Id, User, Telefone e Email.
 */        
        int setar = TabelaProntuario.getSelectedRow();

        ID.setText(TabelaProntuario.getModel().getValueAt(setar, 0).toString());
        
           ID_Paciente.setText(TabelaProntuario.getModel().getValueAt(setar, 1 ).toString());
         
        Diagnostico.setText(TabelaProntuario.getModel().getValueAt(setar, 2).toString());
        Retorno.setText(TabelaProntuario.getModel().getValueAt(setar, 3).toString());
        Remedios.setText(TabelaProntuario.getModel().getValueAt(setar, 4).toString());
        ID_Consulta.setText(TabelaProntuario.getModel().getValueAt(setar, 5).toString());
     
    }

